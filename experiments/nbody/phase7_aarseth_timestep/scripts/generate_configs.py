#!/usr/bin/env python3
"""generate_configs.py — Genera las 54 configuraciones TOML de la Fase 7.

Matriz: 3 distribuciones × 2 tamaños (N=200, N=1000) × 9 variantes.

Distribuciones (idénticas a Fase 6 para comparación directa):
    - plummer_a1:  Plummer a=0.05 (a/ε=1, caótico denso)   ← crítico
    - plummer_a2:  Plummer a=0.10 (a/ε=2, caótico moderado)
    - uniform:     esfera uniforme r=1.0 (caos débil, comparador)

Variantes (9) por par (distribución, N):
    1. fixed_dt025     — KDK + dt=0.025, 1000 pasos   (baseline, idéntico Fase 5/6)
    2. fixed_dt0125    — KDK + dt=0.0125, 2000 pasos  (control: mismo T, dt/2)
    3. fixed_dt00625   — KDK + dt=0.00625, 4000 pasos (control: mismo T, dt/4)
    4. hier_acc_eta001 — KDK block-TS + criterio acc + η=0.01
    5. hier_acc_eta002 — KDK block-TS + criterio acc + η=0.02
    6. hier_acc_eta005 — KDK block-TS + criterio acc + η=0.05
    7. hier_jerk_eta001 — KDK block-TS + criterio jerk + η=0.01
    8. hier_jerk_eta002 — KDK block-TS + criterio jerk + η=0.02
    9. hier_jerk_eta005 — KDK block-TS + criterio jerk + η=0.05

Todos los runs comparten:
  - Solver V5 idéntico al de Fase 6 (relative MAC, softened multipoles)
  - Tiempo total T = 25 (dt_base × num_steps = 0.025 × 1000)
  - 100 snapshots por run (igualmente espaciados en T)

Los TOMLs se escriben en `config/generated/<tag>.toml`.
"""
from __future__ import annotations

from pathlib import Path
import sys

EXP_DIR = Path(__file__).resolve().parents[1]
GEN_DIR = EXP_DIR / "config" / "generated"

# ── Parámetros del solver V5 (idéntico a Fase 6) ─────────────────────────────
SOFTENING = 0.05
G = 1.0
THETA = 0.5
ERR_TOL = 0.005
MULTIPOLE_ORDER = 3

# ── Parámetros de simulación ──────────────────────────────────────────────────
DT_BASE = 0.025          # paso base y dt de los runs fijos baseline
T_TOTAL = 25.0           # tiempo total de simulación
N_SNAPSHOTS = 100        # número de snapshots por run (mismo tiempo físico)

N_STEPS_BASE = int(T_TOTAL / DT_BASE)          # 1000
SNAP_BASE = N_STEPS_BASE // N_SNAPSHOTS         # 10

DT_HALF = DT_BASE / 2                          # 0.0125
N_STEPS_HALF = int(T_TOTAL / DT_HALF)          # 2000
SNAP_HALF = N_STEPS_HALF // N_SNAPSHOTS         # 20

DT_QUARTER = DT_BASE / 4                       # 0.00625
N_STEPS_QUARTER = int(T_TOTAL / DT_QUARTER)    # 4000
SNAP_QUARTER = N_STEPS_QUARTER // N_SNAPSHOTS  # 40

# ── Hierarchical timestep ─────────────────────────────────────────────────────
MAX_LEVEL = 6   # 2^6 = 64 sub-pasos; dt_min ≈ 3.9e-4

N_VALUES = [200, 1000]

DISTRIBUTIONS = [
    # (id, box_size, ic_toml_line, seed)
    ("plummer_a1", 20.0, 'kind = { plummer = { a = 0.05 } }', 11),
    ("plummer_a2", 20.0, 'kind = { plummer = { a = 0.10 } }', 7),
    ("uniform",     4.0, 'kind = { uniform_sphere = { r = 1.0 } }', 42),
]

ETA_VALUES = [0.01, 0.02, 0.05]
CRITERIA = ["acceleration", "jerk"]


# ── Plantillas TOML ───────────────────────────────────────────────────────────

GRAVITY_BLOCK = f"""\
[gravity]
solver              = "barnes_hut"
theta               = {THETA}
multipole_order     = {MULTIPOLE_ORDER}
opening_criterion   = "relative"
err_tol_force_acc   = {ERR_TOL}
softened_multipoles = true
mac_softening       = "consistent"
"""

OUTPUT_BLOCK_TEMPLATE = """\
[output]
snapshot_format   = "jsonl"
snapshot_interval = {snap}
"""

PERFORMANCE_BLOCK = """\
[performance]
deterministic = true
"""


def sim_block(n, box, dt, num_steps, seed, integrator="leapfrog"):
    return f"""\
[simulation]
particle_count         = {n}
box_size               = {box}
dt                     = {dt}
num_steps              = {num_steps}
softening              = {SOFTENING}
gravitational_constant = {G}
seed                   = {seed}
integrator             = "{integrator}"
"""


def toml_fixed_dt(dist_id, box, ic_line, seed, n, dt, num_steps, snap):
    return (
        f"# Generated by phase7/generate_configs.py\n"
        f"# dist={dist_id}  N={n}  variant=fixed_dt  dt={dt}\n"
        f"# Solver V5, T=25, {n_snapshots_str(num_steps, snap)} snapshots\n\n"
        + sim_block(n, box, dt, num_steps, seed)
        + f"\n[initial_conditions]\n{ic_line}\n\n"
        + GRAVITY_BLOCK + "\n"
        + OUTPUT_BLOCK_TEMPLATE.format(snap=snap) + "\n"
        + PERFORMANCE_BLOCK
    )


def toml_hierarchical(dist_id, box, ic_line, seed, n, criterion, eta):
    return (
        f"# Generated by phase7/generate_configs.py\n"
        f"# dist={dist_id}  N={n}  variant=hier_{criterion[:3]}_eta{int(eta*1000):03d}\n"
        f"# Solver V5, block timesteps, criterion={criterion}, eta={eta}\n\n"
        + sim_block(n, box, DT_BASE, N_STEPS_BASE, seed)
        + f"\n[initial_conditions]\n{ic_line}\n\n"
        + f"[timestep]\n"
        + f"hierarchical = true\n"
        + f'criterion    = "{criterion}"\n'
        + f"eta          = {eta}\n"
        + f"max_level    = {MAX_LEVEL}\n\n"
        + GRAVITY_BLOCK + "\n"
        + OUTPUT_BLOCK_TEMPLATE.format(snap=SNAP_BASE) + "\n"
        + PERFORMANCE_BLOCK
    )


def n_snapshots_str(num_steps, snap):
    return num_steps // snap


def eta_tag(eta: float) -> str:
    """Convierte eta a sufijo de nombre: 0.01→eta001, 0.02→eta002, 0.05→eta005."""
    return f"eta{int(round(eta * 100)):03d}"


def main() -> int:
    GEN_DIR.mkdir(parents=True, exist_ok=True)
    count = 0

    for (dist_id, box, ic_line, seed) in DISTRIBUTIONS:
        for n in N_VALUES:
            prefix = f"{dist_id}_N{n}"

            # ── Variante 1: baseline dt=0.025 ─────────────────────────────────
            tag = f"{prefix}_fixed_dt025"
            toml = toml_fixed_dt(dist_id, box, ic_line, seed, n,
                                 DT_BASE, N_STEPS_BASE, SNAP_BASE)
            (GEN_DIR / f"{tag}.toml").write_text(toml)
            count += 1

            # ── Variante 2: control dt=0.0125 ────────────────────────────────
            tag = f"{prefix}_fixed_dt0125"
            toml = toml_fixed_dt(dist_id, box, ic_line, seed, n,
                                 DT_HALF, N_STEPS_HALF, SNAP_HALF)
            (GEN_DIR / f"{tag}.toml").write_text(toml)
            count += 1

            # ── Variante 3: control dt=0.00625 ───────────────────────────────
            tag = f"{prefix}_fixed_dt00625"
            toml = toml_fixed_dt(dist_id, box, ic_line, seed, n,
                                 DT_QUARTER, N_STEPS_QUARTER, SNAP_QUARTER)
            (GEN_DIR / f"{tag}.toml").write_text(toml)
            count += 1

            # ── Variantes 4–6: hierarchical acc ──────────────────────────────
            for eta in ETA_VALUES:
                tag = f"{prefix}_hier_acc_{eta_tag(eta)}"
                toml = toml_hierarchical(dist_id, box, ic_line, seed, n,
                                         "acceleration", eta)
                (GEN_DIR / f"{tag}.toml").write_text(toml)
                count += 1

            # ── Variantes 7–9: hierarchical jerk ─────────────────────────────
            for eta in ETA_VALUES:
                tag = f"{prefix}_hier_jerk_{eta_tag(eta)}"
                toml = toml_hierarchical(dist_id, box, ic_line, seed, n,
                                         "jerk", eta)
                (GEN_DIR / f"{tag}.toml").write_text(toml)
                count += 1

    print(f"Generadas {count} configuraciones en {GEN_DIR}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
