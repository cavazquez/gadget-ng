#!/usr/bin/env python3
"""generate_configs.py — Genera las 40 configuraciones TOML de la Fase 5.

Matriz: 4 distribuciones × 2 tamaños (N=200, N=1000) × 5 variantes MAC.

Las distribuciones son:
    - plummer_a1:   Plummer con a = 0.05  (a/ε = 1, concentrado)
    - plummer_a2:   Plummer con a = 0.10  (a/ε = 2)
    - plummer_a6:   Plummer con a = 0.30  (a/ε = 6, extendido)
    - uniform:      esfera uniforme de radio 1.0

Las 5 variantes MAC se corresponden con las del test local `bh_mac_softening_ablation`:
    V1_geom_bare:            opening=geometric,  softened_multipoles=false
    V2_geom_soft:            opening=geometric,  softened_multipoles=true
    V3_rel_bare:             opening=relative,   softened_multipoles=false, mac_softening=bare
    V4_rel_soft:             opening=relative,   softened_multipoles=true,  mac_softening=bare
    V5_rel_soft_consistent:  opening=relative,   softened_multipoles=true,  mac_softening=consistent

Los TOML generados viven en `config/generated/{dist}_{N}_{variant}.toml`.
"""
from __future__ import annotations

from pathlib import Path
import sys

EXP_DIR = Path(__file__).resolve().parents[1]
GEN_DIR = EXP_DIR / "config" / "generated"

# Parámetros físicos comunes (derivados de plummer_stability/config/serial.toml).
SOFTENING = 0.05
DT = 0.025
NUM_STEPS = 1000
SNAPSHOT_INTERVAL = 10    # 100 frames en total
G = 1.0
THETA = 0.5
ERR_TOL = 0.005

# Tamaños y distribuciones.
N_VALUES = [200, 1000]

DISTRIBUTIONS = [
    # (dist_id, box_size, ic_toml, seed)
    ("plummer_a1", 20.0, 'kind = { plummer = { a = 0.05 } }', 11),
    ("plummer_a2", 20.0, 'kind = { plummer = { a = 0.10 } }', 7),
    ("plummer_a6", 20.0, 'kind = { plummer = { a = 0.30 } }', 13),
    ("uniform",     4.0, 'kind = { uniform_sphere = { r = 1.0 } }', 42),
]

VARIANTS = [
    # (id, opening_criterion, softened_multipoles, mac_softening)
    ("V1_geom_bare",            "geometric", False, "bare"),
    ("V2_geom_soft",            "geometric", True,  "bare"),
    ("V3_rel_bare",             "relative",  False, "bare"),
    ("V4_rel_soft",             "relative",  True,  "bare"),
    ("V5_rel_soft_consistent",  "relative",  True,  "consistent"),
]


def toml_for_run(dist_id: str, box_size: float, ic_line: str, seed: int,
                 n: int, variant_id: str, opening: str, softened: bool,
                 mac_softening: str) -> str:
    softened_str = "true" if softened else "false"
    return f"""# Generated by generate_configs.py — Phase 5
# dist={dist_id}  N={n}  variant={variant_id}

[simulation]
particle_count         = {n}
box_size               = {box_size}
dt                     = {DT}
num_steps              = {NUM_STEPS}
softening              = {SOFTENING}
gravitational_constant = {G}
seed                   = {seed}

[initial_conditions]
{ic_line}

[gravity]
solver              = "barnes_hut"
theta               = {THETA}
multipole_order     = 3
opening_criterion   = "{opening}"
err_tol_force_acc   = {ERR_TOL}
softened_multipoles = {softened_str}
mac_softening       = "{mac_softening}"

[output]
snapshot_format   = "jsonl"
snapshot_interval = {SNAPSHOT_INTERVAL}

[performance]
deterministic = true
"""


def main() -> int:
    GEN_DIR.mkdir(parents=True, exist_ok=True)

    count = 0
    for (dist_id, box, ic_line, seed) in DISTRIBUTIONS:
        for n in N_VALUES:
            for (vid, opening, softened, mac_soft) in VARIANTS:
                toml = toml_for_run(
                    dist_id, box, ic_line, seed, n, vid, opening, softened, mac_soft,
                )
                out = GEN_DIR / f"{dist_id}_N{n}_{vid}.toml"
                out.write_text(toml)
                count += 1

    print(f"Generadas {count} configuraciones en {GEN_DIR}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
