#!/usr/bin/env python3
"""generate_configs.py — Genera las 12 configuraciones TOML de la Fase 6.

Matriz: 3 distribuciones × 2 tamaños (N=200, N=1000) × 2 integradores.

Distribuciones (subset de la Fase 5, seleccionadas por su régimen dinámico):
    - plummer_a1:   Plummer con a = 0.05  (a/ε = 1, caótico denso)  ← crítico
    - plummer_a2:   Plummer con a = 0.10  (a/ε = 2, caótico moderado)
    - uniform:      esfera uniforme de radio 1.0 (caos débil, comparador)

Integradores:
    - leapfrog:  baseline (2º orden, 2 force evals/step)
    - yoshida4:  simpléctico 4º orden (4 force evals/step)

Solver fijo: V5 de Fase 5 (relative + softened_multipoles + mac_softening=consistent,
err_tol_force_acc=0.005). Esto aísla el efecto del integrador y garantiza que
el drift remanente es integrador-limitado y no solver-limitado.

dt fijo = 0.025 (idéntico a Fase 5 para comparación directa). El barrido dt se
realiza en los tests unitarios de `gadget-ng-integrators` sobre sistemas
integrables (oscilador armónico, Kepler).

Los TOML generados viven en `config/generated/{dist}_N{n}_{integrator}.toml`.
"""
from __future__ import annotations

from pathlib import Path
import sys

EXP_DIR = Path(__file__).resolve().parents[1]
GEN_DIR = EXP_DIR / "config" / "generated"

SOFTENING = 0.05
DT = 0.025
NUM_STEPS = 1000
SNAPSHOT_INTERVAL = 10
G = 1.0
THETA = 0.5
ERR_TOL = 0.005

N_VALUES = [200, 1000]

DISTRIBUTIONS = [
    ("plummer_a1", 20.0, 'kind = { plummer = { a = 0.05 } }', 11),
    ("plummer_a2", 20.0, 'kind = { plummer = { a = 0.10 } }', 7),
    ("uniform",     4.0, 'kind = { uniform_sphere = { r = 1.0 } }', 42),
]

INTEGRATORS = [
    ("leapfrog",),
    ("yoshida4",),
]


def toml_for_run(
    dist_id: str,
    box_size: float,
    ic_line: str,
    seed: int,
    n: int,
    integrator: str,
) -> str:
    return f"""# Generated by phase6/generate_configs.py
# dist={dist_id}  N={n}  integrator={integrator}
# Solver fijo: V5 (relative + softened + mac_softening=consistent)

[simulation]
particle_count         = {n}
box_size               = {box_size}
dt                     = {DT}
num_steps              = {NUM_STEPS}
softening              = {SOFTENING}
gravitational_constant = {G}
seed                   = {seed}
integrator             = "{integrator}"

[initial_conditions]
{ic_line}

[gravity]
solver              = "barnes_hut"
theta               = {THETA}
multipole_order     = 3
opening_criterion   = "relative"
err_tol_force_acc   = {ERR_TOL}
softened_multipoles = true
mac_softening       = "consistent"

[output]
snapshot_format   = "jsonl"
snapshot_interval = {SNAPSHOT_INTERVAL}

[performance]
deterministic = true
"""


def main() -> int:
    GEN_DIR.mkdir(parents=True, exist_ok=True)
    count = 0
    for (dist_id, box, ic_line, seed) in DISTRIBUTIONS:
        for n in N_VALUES:
            for (integrator,) in INTEGRATORS:
                toml = toml_for_run(dist_id, box, ic_line, seed, n, integrator)
                out = GEN_DIR / f"{dist_id}_N{n}_{integrator}.toml"
                out.write_text(toml)
                count += 1
    print(f"Generadas {count} configuraciones en {GEN_DIR}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
